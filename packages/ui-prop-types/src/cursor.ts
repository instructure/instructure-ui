import PropTypes from 'prop-types'

/**
 * ---
 * category: utilities/PropTypes
 * ---
 * Verify that the given prop is a valid css `cursor` value.
 *
 * The list of possible cursor values is taken from
 * https://developer.mozilla.org/en-US/docs/Web/CSS/cursor.
 *
 * Note that this does not currently support `url(...)` values.
 * @module cursor
 */
const cursor = PropTypes.oneOf([
  'auto',
  'default',
  'none',
  'context-menu',
  'help',
  'pointer',
  'progress',
  'wait',
  'cell',
  'crosshair',
  'text',
  'vertical-text',
  'alias',
  'copy',
  'move',
  'no-drop',
  'not-allowed',
  'grab',
  'grabbing',
  'all-scroll',
  'col-resize',
  'row-resize',
  'n-resize',
  'e-resize',
  's-resize',
  'w-resize',
  'ne-resize',
  'nw-resize',
  'se-resize',
  'sw-resize',
  'ew-resize',
  'ns-resize',
  'nesw-resize',
  'nwse-resize',
  'zoom-in',
  'zoom-out'
])

export default cursor
export { cursor }
